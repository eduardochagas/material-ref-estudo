print('-----Metodos de string com valores boleanos--------')
print('---- ou seja, retorna valor True ou False ----')


nome = input('Digite seu nome: ')
print('isalpha() = verifica se o valor é alfabetico: ', nome.isalpha())
print('isdecimal() = verifica se o valor é decimal, ou seja, 10, 20, 30...: ', nome.isdecimal())
print('isdigit() = verifica se o valor é numero: ', nome.isdigit())
print('isnumeric() = verifica se o valor é numérico: ', nome.isnumeric())
print('islower() = verifica se a string está toda em minúscula: ', nome.islower())
print('isupper() = verifica se a string está toda em maiúscula: ', nome.isupper())
print('istitile() = verifica se a cada primeira letra de cada palavra está em maiúscula: ', nome.istitle())
print('isspace() = verifica se a variável é vazia: ', nome.isspace())
